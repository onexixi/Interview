    //滑动窗口
    private static int lengthslidingWindow(String s) {
        char[] chars = s.toCharArray ();
        int length = s.length ();
        HashSet set = new HashSet ();
        int i = 0;
        int j = 0;
        int sum = 0;
        while (i < length && j < length) {
            if (!set.contains (chars[j])) {
                set.add (s.charAt (j));
                j++;
                sum = Math.max (sum, j - i + 1);
            } else {
                set.remove (s.charAt (i));
                i++;
            }


        }


        return sum;
    }

    private static int lengthOfLongestSubstring(String s) {
        int sum = 0;
        char[] chars = s.toCharArray ();
        for (int i = 0; i <= chars.length; i++) {
            for (int j = i; j <= chars.length; j++) {
                if (uniqueStr (s, i, j)) {
                    if (sum < j - i) {
                        sum = j - i;
                    }
                }
            }
        }
        return sum;
    }

    public static boolean uniqueStr(String s, int i, int j) {
        Set<Character> set = new HashSet ();
        for (int n = i; n < j; n++) {
            if (set.contains (s.charAt (n))) {
                return false;
            }
            set.add (s.charAt (n));
        }
        return true;
    }

